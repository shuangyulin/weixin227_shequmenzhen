		async onLoad(options) {
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
			// ss读取


			// 下2
			res = await this.$api.option(`yonghu`,`zhanghao`,{});
			this.zhanghaoOptions = res.data;

			// 如果有登陆，获取登陆后保存的userid
			this.ruleForm.userid = uni.getStorageSync("userid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`feiyongxinxi`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			this.cross = options.cross;
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='feiyongbianhao'){
					this.ruleForm.feiyongbianhao = obj[o];
					this.ro.feiyongbianhao = true;
					continue;
					}
					if(o=='feiyongleixing'){
					this.ruleForm.feiyongleixing = obj[o];
					this.ro.feiyongleixing = true;
					continue;
					}
					if(o=='feiyongjine'){
					this.ruleForm.feiyongjine = obj[o];
					this.ro.feiyongjine = true;
					continue;
					}
					if(o=='feiyongmiaoshu'){
					this.ruleForm.feiyongmiaoshu = obj[o];
					this.ro.feiyongmiaoshu = true;
					continue;
					}
					if(o=='feiyongtupian'){
					this.ruleForm.feiyongtupian = obj[o];
					this.ro.feiyongtupian = true;
					continue;
					}
